# this is a parser for the gv500MA
# dependencies are kept to a minimum
# bitstream and crcmod are both required
import rsp_handler as rsp
import evt_handler as evt
def parse_rt(message):
    header = message[0:8]
    byte_header = bytes.fromhex(header)
    ascii_header = byte_header.decode('ASCII')
    print(ascii_header)
    print(message)
    if  ascii_header == '+RSP':
        print(ascii_header,'RSP')
        dict = rsp.parse_aschex_report_rsp(message)
        print('finished parsing :', ascii_header)
        return dict

    if ascii_header == '+EVT':
        print("Event")
        dict = evt.parse_aschex_report_evt(message)
        print('finished parsing :', ascii_header)
        return dict

    if  ascii_header == '+BSP':
        print(ascii_header,'buffered rsp')
        dict = rsp.parse_aschex_report_rsp(message)
        print('finished parsing :', ascii_header)
        return dict

    if ascii_header == '+BVT':
        print("Buffered Event")
        dict = evt.parse_aschex_report_evt(message)
        print('finished parsing :', ascii_header)
        return dict
    if ascii_header == '+HBD':
        print("Heartbeat data")


